from transformers import pipeline

model = pipeline(
        "summarization", 
        model="lcw99/t5-base-korean-text-summary", 
        tokenizer="lcw99/t5-base-korean-text-summary")

text = """
최근 몇 년간 인공지능(AI) 기술은 빠르게 발전하며 다양한 산업 분야에 큰 영향을 미치고 있다. 특히 교육 분야에서의 AI 활용은 학생들의 학습 방식과 교사의 역할, 교육 시스템 전반에 걸쳐 혁신적인 변화를 일으키고 있다. 과거에는 교사 중심의 일방적인 지식 전달 방식이 주를 이루었으나, 현재는 AI 기술을 활용한 맞춤형 학습과 상호작용 기반 교육이 확대되고 있다.
AI 기술은 학생 개개인의 학습 데이터를 분석하여 각자에게 적합한 학습 자료를 제공할 수 있다. 예를 들어, 학습자의 수준, 흥미, 학습 속도 등을 바탕으로 개인화된 콘텐츠를 추천함으로써 학습 효율성을 높일 수 있다. 또한, AI 기반의 챗봇이나 튜터 프로그램은 24시간 학습자의 질문에 응답하고, 부족한 개념을 반복적으로 설명함으로써 보조 교사의 역할을 수행할 수 있다.
더불어, AI는 교사들의 업무 부담을 줄이는 데도 도움을 준다. 출석 체크, 시험 채점, 과제 평가 등 반복적이고 시간 소모적인 업무를 자동화함으로써 교사는 보다 창의적이고 상호작용적인 수업에 집중할 수 있다. 이러한 변화는 교육의 질을 향상시키는 데 긍정적인 영향을 미친다.
하지만 AI 교육의 확산에는 몇 가지 우려도 존재한다. 우선, 모든 학생이 AI 기반 학습 시스템을 동일하게 활용할 수 있는 기술적 환경이 마련되어야 한다. 또한, AI가 판단하는 방식의 투명성과 공정성 문제도 지속적으로 논의되어야 한다. 특히 AI가 편향된 데이터를 바탕으로 잘못된 피드백을 제공할 경우, 학습자에게 부정적인 영향을 미칠 수 있다."
결론적으로, AI는 교육의 새로운 가능성을 열어주고 있지만, 기술의 도입과 활용에는 신중한 접근이 필요하다. 향후에는 기술과 인간 교사의 협력이 더욱 중요해질 것이며, 이를 통해 보다 효율적이고 공정한 교육 환경이 조성될 수 있을 것이다.
"""

summary = model(
    text,
    max_length=200,
    min_length=20,
    truncation=False,
    do_sample=False
)

print(summary[0]["summary_text"])